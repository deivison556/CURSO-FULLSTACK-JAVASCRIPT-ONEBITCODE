// O Que são variáveis
/*
  Variáveis servem para armazenar os dados 
  para que possamos usá-los em nosso código
*/

// 01 - Criando variáveis
// Existem 3 formas de criar variáveis: var, let e const
var nome = 'Deivison'; // var é o menos utilizado hoje em dia
console.log(nome);

let sobrenome = 'Gracindo'; // let é o mais utilizado hoje em dia
console.log(sobrenome);

const idade = 27; // const é uma variavel constante que o dado não muda
console.log(idade);

